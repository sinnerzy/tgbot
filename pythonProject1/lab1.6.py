def season_events():
    months = {
        1: ('январе', 'зима'),
        2: ('феврале', 'зима'),
        3: ('марте', 'весна'),
        4: ('апреле', 'весна'),
        5: ('мае', 'весна'),
        6: ('июне', 'лето'),
        7: ('июле', 'лето'),
        8: ('августе', 'лето'),
        9: ('сентябре', 'осень'),
        10: ('октябре', 'осень'),
        11: ('ноябре', 'осень'),
        12: ('декабре', 'зима')
    }

    season_descriptions = {
        'зима': 'За окном падал белый снег',
        'весна': 'Птицы пели прекрасные песни',
        'лето': 'Солнце светило ярче чем когда-либо',
        'осень': 'Урожай был невероятным'
    }

    while True:
        user_input = input("Введите номер месяца вашего рождения (1-12): ")

        try:
            month_num = int(user_input)
            if 1 <= month_num <= 12:
                month_name, season = months[month_num]
                print(f"Вы родились в {month_name}. {season_descriptions[season]}.")
                break
            else:
                print("Ошибка: номер месяца должен быть от 1 до 12.")
        except ValueError:
            print("Ошибка: введите целое число от 1 до 12.")


# Запуск функции
season_events()